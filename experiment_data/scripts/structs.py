            tool_to_reductions = {}
            for tool in tools:
                reductions = []
                for (pid, qid) in self.content[datasize].keys():
                    res = self.content[datasize][(pid, qid)]
                    tool_can_output = res.tool_can_output(tool)
                    tool_can_rewrite = res.tool_can_rewrite(tool)
                    tool_can_optimize = res.tool_can_optimize(
                        tool, measure)
                    if tool_can_output:
                        if res.get_input_measure_value(measure) == timeout[workload] and res.get_tool_measure_value(tool, measure) == timeout[workload]:
                            pass
                        else:
                            reduction = self.calc_reduction(
                                res.get_input_measure_value(measure),
                                res.get_tool_measure_value(tool, measure)
                            )
