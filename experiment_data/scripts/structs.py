    def plot_reduction_stats_SC_vs_baseline(self):

        datasizes = list(self.content.keys())
        baselines = ['LR', 'WT']
        percentiles = [50, 75, 90]
        measures = ['time']

        for (baseline, measure) in itertools.product(baselines, measures):
            # collect
            SC_reduction_list = []
            baseline_reduction_list = []
            for datasize in datasizes:
                SC_reductions = []
                baseline_reductions = []
                for (pid, qid) in self.content[datasize].keys():
                    res = self.content[datasize][(pid, qid)]
                    SC_can_rewrite = res.tool_can_rewrite('SC')
                    SC_can_optimize = res.tool_can_optimize('SC', measure)
                    baseline_can_rewrite = res.tool_can_rewrite(baseline)
                    baseline_can_optimize = res.tool_can_optimize(
                        baseline, measure)

                    if SC_can_rewrite:
                        SC_reductions.append(
                            res.get_input_measure_value(measure) /
                            res.get_tool_measure_value('SC', measure)
                        )
                    if baseline_can_rewrite:
                        baseline_reductions.append(
                            res.get_input_measure_value(measure) /
                            res.get_tool_measure_value(baseline, measure)
                        )

                SC_reduction_list.append(SC_reductions)
                baseline_reduction_list.append(baseline_reductions)

            # plot
            xs = np.array([i * 1.2 for i in range(0, len(datasizes))])
            total_width = 0.5
            bar_width = 0.15
            line_width = bar_width / 2
            bar_thickness = 0.6
            line_thickness = 1
            gap = 0.01

            fig, ax = plt.subplots()
            set_figure_size(fig, 5, 3)

            for (i, x) in enumerate(xs):

                SC_reductions = SC_reduction_list[i]
                baseline_reductions = baseline_reduction_list[i]

