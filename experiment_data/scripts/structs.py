class Result:

    def __init__(self):
        # ('input', measure) -> (value, input query text)
        # we always have an entry for input
        # ('SC', measure) -> (value, output query text)
        # ('LR', measure) -> (value, output query text)
        # ('WT', measure) -> (value, output query text)
        # ('GT', measure) -> (value, output query text)
        # if a tool does not produce an output, the mapping will not have an entry for the tool
        # note that, if GT has an output, it always has a smaller cost/time than the input query;
        # otherwise, we do not have an entry for GT
        self.content = {}        
        
    def set_input_measure_query(self, measure, value, query):
        # the same entry should not be set more than once
        assert ('input', measure) not in self.content.keys()
        self.content[('input', measure)] = (float(value), query)

    def get_input_measure_value(self, measure):
        return self.content[('input', measure)][0]

    def get_input_query(self):
        return self.content[('input', 'cost')][1]

    def set_tool_measure_query(self, tool, measure, value, query):
        # the same entry should not be set more than once
        assert (tool, measure) not in self.content.keys()
        self.content[(tool, measure)] = (float(value), query)

    # return cost/time (depending on the "measure" parameter) of the output query
    # generated by tool (specified by the "tool" parameter)
    def get_tool_measure_value(self, tool, measure):
        if (tool, measure) in self.content.keys():
            return self.content[(tool, measure)][0]
        else:
            return ''

    # return the output query text generated by tool (specified by the "tool" parameter)
    # note that the "measure" parameter is included here mostly due to GT
    def get_tool_measure_output_query(self, tool, measure):
        if (tool, measure) in self.content.keys():
            return self.content[(tool, measure)][1]
        else:
            return ''

    # tool can generate an output query which may have the same plan as the input query
    # even if the plan is different, the output may not have smaller cost/time
    def tool_can_output(self, tool):
        if tool == 'GT':
            return self.tool_can_optimize(tool, 'cost') or self.tool_can_optimize(tool, 'time')

        assert tool in ['SC', 'LR', 'WT'], tool
        return (tool, 'cost') in self.content.keys()

    def print_tool_can_output(self, tool):
        return 'Y' if self.tool_can_output(tool) else 'N'

    # tool can rewrite the input query to an output with a different plan
    # though the output may not have smaller cost/time
    def tool_can_rewrite(self, tool):
        if tool == 'GT':
            return self.tool_can_optimize(tool, 'cost') or self.tool_can_optimize(tool, 'time')

        assert tool in ['SC', 'LR', 'WT'], tool
        # (1) can rewrite and (2) rewrite to an output with a different query plan
        return self.tool_can_output(tool) and self.content[(tool, 'cost')][0] != self.content[('input', 'cost')][0]

    def print_tool_can_rewrite(self, tool):
        return 'Y' if self.tool_can_rewrite(tool) else 'N'

    # tool can rewrite the input to an output with a different plan and the output
    # has smaller cost or time (specified by the measure parameter)
    def tool_can_optimize(self, tool, measure):
        if tool == 'GT':
            return (tool, measure) in self.content.keys()

        assert tool in ['SC', 'LR', 'WT'], tool
        if measure == 'cost':
            return self.tool_can_rewrite(tool) \
                and self.content[(tool, measure)][0] < self.content[('input', measure)][0]
        elif measure == 'time':
            # assert opt_threshold == 1
            return self.tool_can_rewrite(tool) \
                and self.content[(tool, measure)][0] < self.content[('input', measure)][0]
        else:
            assert False

    def print_tool_can_optimize(self, tool, measure):
        return 'Y' if self.tool_can_optimize(tool, measure) else 'N'


class ResultSet:

    # evaluation result for one setting

    def __init__(self, workload):
        # workload -> LeetCode or Calcite

        self.setting = {
            'workload': workload,
        }
        # { datasize -> { (problem_id, query_id) -> Result } }
        self.content = {}

    def size(self):
        ret = len(self.content[next(iter(self.content))])
        for datasize in self.content.keys():
            assert ret == len(self.content[datasize])
        return ret

    def get_workload(self):
        return self.setting['workload']

    # add one entire result
    def add_result(self, datasize, problem_id, query_id, result):
        if datasize not in self.content.keys():
            self.content[datasize] = {}
        assert (problem_id, query_id) not in self.content[datasize].keys()
        self.content[datasize][(problem_id, query_id)] = result
   
    def calc_reduction(self, numerator, denominator):
        if type(numerator) == str or type(denominator) == str:
            return '-'
        else:
            return numerator / denominator
        
    def __make_result(self, datasize, problem_id, query_id):
        if datasize not in self.content.keys():
            self.content[datasize] = {}
        if (problem_id, query_id) not in self.content[datasize].keys():
            self.content[datasize][(problem_id, query_id)] = Result()
        return self.content[datasize][(problem_id, query_id)]

    def add_input_measure(self, datasize, problem_id, query_id, measure, value, query):
        result = self.__make_result(datasize, problem_id, query_id)
        result.set_input_measure_query(measure, value, query)

    def add_tool_measure(self, datasize, problem_id, query_id, tool, measure, value, query):
        result = self.__make_result(datasize, problem_id, query_id)
        result.set_tool_measure_query(tool, measure, value, query)

    def set_GT_results(self):

        for datasize in self.content.keys():

            # set GT cost
            pid_to_min_input_cost = {}
            for (pid, qid) in self.content[datasize].keys():
                res = self.content[datasize][(pid, qid)]
                input_cost = res.get_input_measure_value('cost')
                input_query = res.get_input_query()
                if pid in pid_to_min_input_cost.keys():
                    (cur_min_cost, _) = pid_to_min_input_cost[pid]
                    if input_cost < cur_min_cost:
                        pid_to_min_input_cost[pid] = (input_cost, input_query)
                else:
                    pid_to_min_input_cost[pid] = (input_cost, input_query)
            for (pid, qid) in self.content[datasize].keys():
                res = self.content[datasize][(pid, qid)]
                (min_cost, min_cost_input_query) = pid_to_min_input_cost[pid]
                assert min_cost <= res.get_input_measure_value('cost')
                # set GT result only if the smallest-cost input query has a different query plan
                # (i.e., its cost is different from the cost of this input query)
                if min_cost != res.get_input_measure_value(measure='cost'):
                    res.set_tool_measure_query(
                        tool='GT',
                        measure='cost',
                        value=min_cost,
                        query=min_cost_input_query
                    )

            # set GT time
            for (pid, qid) in self.content[datasize].keys():
                res = self.content[datasize][(pid, qid)]
                input_time = res.get_input_measure_value('time')
                input_cost = res.get_input_measure_value('cost')
                GT_time = input_time
                GT_query = None
                for (pid_1, qid_1) in self.content[datasize].keys():
                    if pid_1 == pid and qid_1 != qid:
                        res_1 = self.content[datasize][(pid_1, qid_1)]
                        input_time_1 = res_1.get_input_measure_value('time')
                        input_query_1 = res_1.get_input_query()
                        input_cost_1 = res_1.get_input_measure_value('cost')
                        if input_time_1 < GT_time and input_cost_1 != input_cost:
                            GT_time = input_time_1
                            GT_query = input_query_1
                if GT_time < input_time:
                    res.set_tool_measure_query(
                        tool='GT',
                        measure='time',
                        value=GT_time,
                        query=GT_query
                    )

    def sanity_check(self):
        for datasize in self.content.keys():
            m = self.content[datasize]
            for (pid, qid) in m.keys():

                res = m[(pid, qid)]

                # input query cost is always non-negative
                assert res.get_input_measure_value('cost') >= 0
                assert res.content['input', 'cost'][0] >= 0
                # input query time is always positive
                assert res.get_input_measure_value('time') > 0
                assert res.content['input', 'time'][0] > 0
                # every benchmark has input query cost
                assert ('input', 'cost') in res.content.keys()
                # every benchmark has input query time
                assert ('input', 'time') in res.content.keys()

                assert res.get_input_query() == res.content['input', 'cost'][1]
                assert res.get_input_query() == res.content['input', 'time'][1]

                for tool in ['SC', 'LR', 'WT']:
                    if res.tool_can_output(tool):
                        assert res.get_tool_measure_value(tool, 'cost') > 0
                        assert res.get_tool_measure_value(tool, 'time') > 0
                        assert res.get_tool_measure_output_query(
                            tool, 'cost') == res.content[tool, 'cost'][1]
                        assert res.get_tool_measure_output_query(
                            tool, 'time') == res.content[tool, 'time'][1]
                        assert res.content[tool,
                                           'cost'][1] == res.content[tool, 'time'][1]
                    if not res.tool_can_output(tool):
                        assert (tool, 'cost') not in res.content.keys()
                        assert (tool, 'time') not in res.content.keys()
                        assert not res.tool_can_rewrite(tool)
                        assert not res.tool_can_optimize(tool, 'cost')
                        assert not res.tool_can_optimize(tool, 'time')

                    if res.tool_can_rewrite(tool):
                        assert res.tool_can_output(tool)
                        assert res.get_tool_measure_value(
                            tool, 'cost') != res.get_input_measure_value('cost')
                    if not res.tool_can_rewrite(tool):
                        assert not res.tool_can_optimize(tool, 'cost')
                        assert not res.tool_can_optimize(tool, 'time')

                    if res.tool_can_optimize(tool, 'cost'):
                        assert res.tool_can_output(tool)
                        assert res.tool_can_rewrite(tool)
                    if res.tool_can_optimize(tool, 'time'):
                        assert res.tool_can_output(tool)
                        assert res.tool_can_rewrite(tool)

                    if res.tool_can_output(tool) and not res.tool_can_rewrite(tool):
                        assert res.get_tool_measure_value(
                            tool, 'cost') == res.get_input_measure_value('cost')

                    # if res.tool_can_output(tool) and res.tool_can_rewrite(tool) and not res.tool_can_optimize(tool, 'cost'):
                    #     assert res.get_tool_measure_value(
                    #         tool, 'cost') > res.get_input_measure_value('cost')
                    # if res.tool_can_output(tool) and res.tool_can_rewrite(tool) and not res.tool_can_optimize(tool, 'time'):
                    #     assert res.get_tool_measure_value(
                    #         tool, 'time') >= res.get_input_measure_value('time')

                if ('GT', 'cost') in res.content.keys():
                    # GT always gives a smaller cost than that of this input query
                    assert res.content['input',
                                       'cost'][0] > res.content['GT', 'cost'][0]
                if ('GT', 'time') in res.content.keys():
                    # GT query always has a smaller time than this query
                    assert res.content['input',
                                       'time'][0] > res.content['GT', 'time'][0]

